# Arithmetic Operators in Python 
# Integers 

print(1 + 2) # Addition
print(2 - 2) # Subtraction 
print(2 * 3) # Multiplication
print(4 / 2) # division that gives floating number
print(6 / 2) # Division 
print(7 / 2) # Division
print(7 // 2) # Division w/o remainder
print(3 % 2) # Modulus 
print(7 // 3) # Division w/o remainder
print(3 ** 3) # Exponential

print(1 + 1j) # Complex numbers 
print((1 + 1j) * (1 - 1j)) # Multiplying complex numbers


